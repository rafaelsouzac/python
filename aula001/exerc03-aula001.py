#Faça um Programa que converta metros para centímetros.

#============== DECLARAÇÕES E ATRIBUIÇÕES =====================
valor_em_metro = 0.00
valor_em_centimetros = 0

#============== ENTRADAS E ATRIBUIÇÕES ========================
valor_em_metro = float(input('Digite o valor em metros: '))

#============== PROCESSAMENTO E ATRIBUIÇÕES ===================
valor_em_centimetros = valor_em_metro * 100

#============== SAÍDA =========================================
print(f'O valor informando em metros: {valor_em_metro} é em centimetros: {valor_em_centimetros}')